
public class Java_lesson4 {
	public static void main(String[] args) {
        final int LUCKY_NUMBER = 37;
//        定数で数字を宣言
        String str = "yoshiya miyamoto";
//        変数で文字列を宣言
        System.out.println(str);
//        yoshiya miyamotoを出力
        str = "水滴石を穿つ";
//        変数strに↑を再代入
        System.out.println(str);
//        再代入したstrを出力
        System.out.println(LUCKY_NUMBER);
//        定数を出力
	}
}
